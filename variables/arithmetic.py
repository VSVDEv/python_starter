# Math
import math

a = 4
b = 2

print(a + b)
print(a - b)
print(a * b)
print(a / b)
# without residue return int
print(a // b)
# residue
print(a % b)
print(-a)
print(+a)

print(abs(a))

print(int(a))
print(float(a))

comp = complex(a, b)
print(comp)

print(comp.conjugate())

print(a**b)

print(pow(a, b))

c = 2.534534534
print(round(c))

print(round(c, 2))

print(math.trunc(c))

print(math.floor(c))

print(math.ceil(c))

print(math.pi)

print(math.e)
print(math.sin(c))
